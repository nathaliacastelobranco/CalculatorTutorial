#include <iostream>
#include "Calculator.h"

using namespace std; //faz com que todas as entradas padrão do c++ não solicitem o std::

int main()
{
    double x = 0.0; //double aceita decimais, iniciando variaveis com formato double
    double y = 0.0;
    double result = 0.0;
    char oper = '+';

    cout << "Calculator Console Application" << endl << endl;
    cout << "Please enter the operation to perform. Format: a+b | a-b | a*b | a/b"
        << endl;

    Calculator c; //declara um objeto chamado C como uma instância da classe Calculator
    while (true)
    {
        cin >> x >> oper >> y;
        if (oper == '/' && y ==0)
        {
            cout << "Division by 0 exception" << endl;
            continue;
        }
        else
        {
            result = c.Calculate(x, oper, y);
        }
        cout << "Result is: " << result << endl;
    }

    return 0;
}

